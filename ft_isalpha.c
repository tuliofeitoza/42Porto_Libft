#include <unistd.h>
#include <stdio.h>

int	ft_isalpha(char alpha);

int	ft_isalpha(char alpha)
{
	if ((alpha >= 'a' && alpha <= 'z') || (alpha >= 'A' && alpha <= 'Z'))
		return (1);
	else
		return (0);
}
